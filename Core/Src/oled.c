/* OLED 使用原理图 PC0/PC1 软件 I2C。 */
static void OLED_I2cDelay(void)
{
  volatile uint8_t i;
  for (i = 0; i < 30U; i++) __NOP();
}

static void OLED_Scl(GPIO_PinState state)
{
  HAL_GPIO_WritePin(OLED_SCL_GPIO_PORT, OLED_SCL_GPIO_PIN, state);
}

static void OLED_Sda(GPIO_PinState state)
{
  HAL_GPIO_WritePin(OLED_SDA_GPIO_PORT, OLED_SDA_GPIO_PIN, state);
}

static void OLED_I2cStart(void)
{
  OLED_Sda(GPIO_PIN_SET);
  OLED_Scl(GPIO_PIN_SET);
  OLED_I2cDelay();
  OLED_Sda(GPIO_PIN_RESET);
  OLED_I2cDelay();
  OLED_Scl(GPIO_PIN_RESET);
}

static void OLED_I2cStop(void)
{
  OLED_Sda(GPIO_PIN_RESET);
  OLED_Scl(GPIO_PIN_SET);
  OLED_I2cDelay();
  OLED_Sda(GPIO_PIN_SET);
  OLED_I2cDelay();
}

/* MCU 单独复位时 OLED 可能仍停在一次未结束的通信中，用 9 个时钟释放总线。 */
static void OLED_I2cRecover(void)
{
  uint8_t count;
  OLED_Sda(GPIO_PIN_SET);
  for (count = 0; count < 9U; count++)
  {
    OLED_Scl(GPIO_PIN_RESET);
    OLED_I2cDelay();
    OLED_Scl(GPIO_PIN_SET);
    OLED_I2cDelay();
  }
  OLED_I2cStop();
}

static void OLED_I2cWriteByte(uint8_t value)
{
  uint8_t bit;
  for (bit = 0; bit < 8U; bit++)
  {
    OLED_Sda((value & 0x80U) ? GPIO_PIN_SET : GPIO_PIN_RESET);
    OLED_I2cDelay();
    OLED_Scl(GPIO_PIN_SET);
    OLED_I2cDelay();
    OLED_Scl(GPIO_PIN_RESET);
    value <<= 1;
  }
  /* 第九个时钟用于从机 ACK；OLED 写入时不需要读取 ACK。 */
  OLED_Sda(GPIO_PIN_SET);
  OLED_Scl(GPIO_PIN_SET);
  OLED_I2cDelay();
  OLED_Scl(GPIO_PIN_RESET);
}

static void OLED_WriteCommand(uint8_t command)
{
  OLED_I2cStart();
  OLED_I2cWriteByte(OLED_I2C_WRITE_ADDRESS);
  OLED_I2cWriteByte(0x00U);
  OLED_I2cWriteByte(command);
  OLED_I2cStop();
}

/* 初始化常见 128x64、I2C 接口的 SSD1306 OLED。 */
void OLED_Init(void)
{
  GPIO_InitTypeDef gpio = {0};
  __HAL_RCC_GPIOC_CLK_ENABLE();
  gpio.Pin = OLED_SCL_GPIO_PIN | OLED_SDA_GPIO_PIN;
  gpio.Mode = GPIO_MODE_OUTPUT_OD;
  gpio.Pull = GPIO_PULLUP;
  gpio.Speed = GPIO_SPEED_FREQ_HIGH;
  HAL_GPIO_Init(OLED_SCL_GPIO_PORT, &gpio);
  OLED_Scl(GPIO_PIN_SET);
  OLED_Sda(GPIO_PIN_SET);
  HAL_Delay(100);
  OLED_I2cRecover();
  HAL_Delay(10);
  OLED_WriteCommand(0xAE); OLED_WriteCommand(0x20); OLED_WriteCommand(0x00);
  OLED_WriteCommand(0xB0); OLED_WriteCommand(0xC8); OLED_WriteCommand(0x00);
  OLED_WriteCommand(0x10); OLED_WriteCommand(0x40); OLED_WriteCommand(0x81);
  OLED_WriteCommand(0x7F); OLED_WriteCommand(0xA1); OLED_WriteCommand(0xA6);
  OLED_WriteCommand(0xA8); OLED_WriteCommand(0x3F); OLED_WriteCommand(0xA4);
  OLED_WriteCommand(0xD3); OLED_WriteCommand(0x00); OLED_WriteCommand(0xD5);
  OLED_WriteCommand(0x80); OLED_WriteCommand(0xD9); OLED_WriteCommand(0xF1);
  OLED_WriteCommand(0xDA); OLED_WriteCommand(0x12); OLED_WriteCommand(0xDB);
  OLED_WriteCommand(0x40); OLED_WriteCommand(0x8D); OLED_WriteCommand(0x14);
  OLED_WriteCommand(0xAF);
  HAL_Delay(20);
  OLED_Clear();
  OLED_Refresh();
}

void OLED_Clear(void)
{
  memset(oled_buffer, 0, sizeof(oled_buffer));
}

/* 在显存中显示一行 5x7 ASCII 字符，page 范围为 0~7。 */
void OLED_ShowString(uint8_t x, uint8_t page, const char *text)
{
  uint8_t column;
  uint8_t character;
  while (*text != '\0' && x < 122U && page < 8U)
  {
    character = (uint8_t)*text++;
    if (character < 32U || character > 126U) character = '?';
    for (column = 0; column < 5U; column++)
      oled_buffer[page * 128U + x + column] = font5x7[character - 32U][column];
    oled_buffer[page * 128U + x + 5U] = 0;
    x += 6U;
  }
}

